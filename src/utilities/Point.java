package utilities;

public class Point {
    private final  double x;
    private final  double y;

    public Point(double x, double y, double x1, double y1)
    {
        this.x = x1;
        this.y = y1;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

}
